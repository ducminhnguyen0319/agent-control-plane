#!/usr/bin/env bash
set -euo pipefail

# migrate-config.sh - Migrate ACP configs to latest format
# Usage: bash tools/bin/migrate-config.sh [--profile-id <id>] [--dry-run]

SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
DRY_RUN=false
PROFILE_ID=""

while [[ $# -gt 0 ]]; do
  case "$1" in
    --profile-id)
      PROFILE_ID="$2"
      shift 2
      ;;
    --dry-run)
      DRY_RUN=true
      shift
      ;;
    *)
      echo "Unknown option: $1"
      exit 1
      ;;
  esac
done

# Colors
GREEN='\033[0;32m'
YELLOW='\033[1;33m'
BLUE='\033[0;34m'
NC='\033[0m'

echo -e "${BLUE}=== ACP Configuration Migration ===${NC}"
echo ""

migrate_profile() {
  local profile_id="$1"
  local profile_dir="$HOME/.agent-runtime/control-plane/profiles/$profile_id"
  local config_file="$profile_dir/control-plane.yaml"
  local env_file="$profile_dir/runtime.env"
  local migrations_applied=0
  
  if [[ ! -d "$profile_dir" ]]; then
    echo -e "${YELLOW}Profile '$profile_id' not found, skipping.${NC}"
    return 0
  fi
  
  echo "Migrating profile: $profile_id"
  
  # Migration 1: Ensure runtime.env exists
  if [[ ! -f "$env_file" ]]; then
    echo "  - Creating runtime.env..."
    if [[ "$DRY_RUN" == "false" ]]; then
      cat > "$env_file" <<EOF
# Runtime environment for $profile_id
# Generated by migrate-config.sh

AGENT_ROOT="${AGENT_ROOT:-$HOME/.agent-runtime/projects/$profile_id}"
RUNS_ROOT="\${AGENT_ROOT}/runs"
STATE_ROOT="\${AGENT_ROOT}/state"
HISTORY_ROOT="\${AGENT_ROOT}/history"
WORKTREE_ROOT="${WORKTREE_ROOT:-$HOME/src/${profile_id}-worktrees}"
EOF
    fi
    ((migrations_applied++))
  fi
  
  # Migration 2: Add missing env vars to runtime.env
  if [[ -f "$env_file" ]]; then
    local vars_added=0
    for var in "AGENT_ROOT" "RUNS_ROOT" "STATE_ROOT" "HISTORY_ROOT"; do
      if ! grep -q "^${var}=" "$env_file" 2>/dev/null; then
        echo "  - Adding $var to runtime.env..."
        if [[ "$DRY_RUN" == "false" ]]; then
          case "$var" in
            AGENT_ROOT) echo "AGENT_ROOT=\"$HOME/.agent-runtime/projects/$profile_id\"" >> "$env_file" ;;
            RUNS_ROOT) echo 'RUNS_ROOT="${AGENT_ROOT}/runs"' >> "$env_file" ;;
            STATE_ROOT) echo 'STATE_ROOT="${AGENT_ROOT}/state"' >> "$env_file" ;;
            HISTORY_ROOT) echo 'HISTORY_ROOT="${AGENT_ROOT}/history"' >> "$env_file" ;;
          esac
        fi
        ((vars_added++))
        ((migrations_applied++))
      fi
    done
  fi
  
  # Migration 3: Backup and update control-plane.yaml if needed
  if [[ -f "$config_file" ]]; then
    # Check if execution section exists
    if ! grep -q "^execution:" "$config_file" 2>/dev/null; then
      echo "  - Adding execution section to control-plane.yaml..."
      if [[ "$DRY_RUN" == "false" ]]; then
        cp "$config_file" "$config_file.backup.$(date +%Y%m%d%H%M%S)"
        cat >> "$config_file" <<EOF

execution:
  coding_worker: codex
  max_concurrent_workers: 20
  max_launches_per_heartbeat: 20
EOF
      fi
      ((migrations_applied++))
    fi
  fi
  
  # Migration 4: Ensure state directories exist
  if [[ -f "$env_file" ]]; then
    source "$env_file"
    for dir in "$AGENT_ROOT" "$RUNS_ROOT" "$STATE_ROOT" "$HISTORY_ROOT"; do
      if [[ -n "$dir" && ! -d "$dir" ]]; then
        echo "  - Creating directory: $dir"
        if [[ "$DRY_RUN" == "false" ]]; then
          mkdir -p "$dir"
        fi
        ((migrations_applied++))
      fi
    done
  fi
  
  if [[ $migrations_applied -gt 0 ]]; then
    echo -e "  ${GREEN}Applied $migrations_applied migration(s)${NC}"
  else
    echo "  Already up to date."
  fi
  echo ""
}

# Run migrations
if [[ -n "$PROFILE_ID" ]]; then
  migrate_profile "$PROFILE_ID"
else
  # Migrate all profiles
  profiles_dir="$HOME/.agent-runtime/control-plane/profiles"
  if [[ -d "$profiles_dir" ]]; then
    for profile_dir in "$profiles_dir"/*; do
      if [[ -d "$profile_dir" ]]; then
        profile_id=$(basename "$profile_dir")
        migrate_profile "$profile_id"
      fi
    done
  else
    echo -e "${YELLOW}No profiles found at $profiles_dir${NC}"
  fi
fi

echo -e "${GREEN}=== Migration complete ===${NC}"
if [[ "$DRY_RUN" == "true" ]]; then
  echo -e "${YELLOW}(Dry run - no changes were made)${NC}"
fi
